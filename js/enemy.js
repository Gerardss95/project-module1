class Enemy {
    constructor(x, y, width, height) {
      this.width = width;
      this.height = height;
      this.x = x;
      this.y = y;
      this.speed = 4;
    }
  
    draw(ctx) {
      this.image = new Image();
      this.image.src = "../IMG/enemy.png";
      ctx.drawImage(this.image, this.x, this.y, this.width, this.height);
    }
  
    enemyTrajectory() {
      this.x = this.x - 4;
    }
  }